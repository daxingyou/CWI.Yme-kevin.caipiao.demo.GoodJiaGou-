
namespace Yme.Mcp.Model.Waimai.Meituan
{
    /// <summary>
    /// 美团订单信息
    /// </summary>
    public class MeituanOrderModel
    {
        /// <summary>
        /// 订单创建时间
        /// </summary>
        public long ctime { get; set; }

        /// <summary>
        /// 平均送达时间
        /// </summary>
        public long avgSendTime { get; set; }

        /// <summary>
        /// 订单备注
        /// </summary>
        public string caution { get; set; }

        /// <summary>
        /// 用户预计送达时间，“立即送达”时为0
        /// </summary>
        public long deliveryTime { get; set; }

        /// <summary>
        /// 订单菜品详情
        /// </summary>
        public string detail { get; set; }

        /// <summary>
        /// 城市Id
        /// </summary>
        public long cityId { get; set; }

        /// <summary>
        /// erp方门店id
        /// </summary>
        public string ePoiId { get; set; }

        /// <summary>
        /// 订单扩展信息
        /// </summary>
        public string extras { get; set; }

        /// <summary>
        /// 是否需要发票:1-需要发票；0-不需要
        /// </summary>
        public int hasInvoiced { get; set; }

        /// <summary>
        /// 发票抬头
        /// </summary>
        public string invoiceTitle { get; set; }

        /// <summary>
        /// 发票税号
        /// </summary>
        public string taxpayerId { get; set; }

        /// <summary>
        /// 是否是预定单:1-预订单；0-非预定
        /// </summary>
        public int isPre { get; set; }

        /// <summary>
        /// 是否第三方配送 0：否；1：是
        /// </summary>
        public int isThirdShipping { get; set; }

        /// <summary>
        /// 实际送餐地址纬度[高德坐标系]
        /// </summary>
        public double latitude { get; set; }

        /// <summary>
        /// 实际送餐地址经度[高德坐标系]
        /// </summary>
        public double longitude { get; set; }

        /// <summary>
        /// 商家对账信息
        /// </summary>
        public string poiReceiveDetail { get; set; }

        #region 配送相关

        /// <summary>
        /// 配送类型码
        /// </summary>
        public int logisticsCode { get; set; }

        /// <summary>
        /// 配送完成时间
        /// </summary>
        public long logisticsCompletedTime { get; set; }

        /// <summary>
        /// 配送单确认时间，骑手接单时间
        /// </summary>
        public long logisticsConfirmTime { get; set; }

        /// <summary>
        /// 骑手电话
        /// </summary>
        public long logisticsDispatcherMobile { get; set; }

        /// <summary>
        /// 骑手电话
        /// </summary>
        public string logisticsDispatcherName { get; set; }

        /// <summary>
        /// 骑手取单时间
        /// </summary>
        public long logisticsFetchTime { get; set; }

        /// <summary>
        /// 配送方ID
        /// </summary>
        public int logisticsId { get; set; }

        /// <summary>
        /// 配送方名称
        /// </summary>
        public string logisticsName { get; set; }

        /// <summary>
        /// 配送单下单时间
        /// </summary>
        public long logisticsSendTime { get; set; }

        /// <summary>
        /// 配送订单状态code
        /// </summary>
        public int logisticsStatus { get; set; }

        #endregion

        /// <summary>
        /// 订单完成时间
        /// </summary>
        public long orderCompletedTime { get; set; }

        /// <summary>
        /// 商户确认时间
        /// </summary>
        public long orderConfirmTime { get; set; }

        /// <summary>
        /// 订单取消时间
        /// </summary>
        public long orderCancelTime { get; set; }

        /// <summary>
        /// 订单Id
        /// </summary>
        public long orderId { get; set; }

        /// <summary>
        /// 订单展示Id
        /// </summary>
        public long orderIdView { get; set; }

        /// <summary>
        /// 用户下单时间
        /// </summary>
        public long orderSendTime { get; set; }

        /// <summary>
        /// 订单原价
        /// </summary>
        public float originalPrice { get; set; }

        /// <summary>
        /// 订单支付类型
        /// </summary>
        public int payType { get; set; }

        #region 门店相关

        /// <summary>
        /// 门店地址
        /// </summary>
        public string poiAddress { get; set; }

        /// <summary>
        /// 门店Id
        /// </summary>
        public long poiId { get; set; }

        /// <summary>
        /// 门店名称
        /// </summary>
        public string poiName { get; set; }

        /// <summary>
        /// 门店服务电话
        /// </summary>
        public string poiPhone { get; set; }

        #endregion

        #region 收货人相关

        /// <summary>
        /// 收货人地址
        /// </summary>
        public string recipientAddress { get; set; }

        /// <summary>
        /// 收货人名称
        /// </summary>
        public string recipientName { get; set; }

        /// <summary>
        /// 收货人电话
        /// </summary>
        public string recipientPhone { get; set; }

        #endregion

        /// <summary>
        /// 骑手电话
        /// </summary>
        public string shipperPhone { get; set; }

        /// <summary>
        /// 配送费用
        /// </summary>
        public float shippingFee { get; set; }

        /// <summary>
        /// 订单状态
        /// </summary>
        public int status { get; set; }

        /// <summary>
        /// 总价
        /// </summary>
        public float total { get; set; }

        /// <summary>
        /// 订单更新时间
        /// </summary>
        public long uTime { get; set; }

        /// <summary>
        /// 门店当天的订单流水号
        /// </summary>
        public int daySeq { get; set; }

        /// <summary>
        /// 就餐人数
        /// </summary>
        public int dinnersNumber { get; set; }
    }
}
