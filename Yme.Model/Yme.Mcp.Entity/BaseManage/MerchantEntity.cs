using System;
using Yme.Code;
using Yme.Util;

namespace Yme.Mcp.Entity.BaseManage
{
    /// <summary>
    /// 商户信息
    /// </summary>
	public class MerchantEntity : BaseEntity
    {
		/// <summary>
		/// 商户ID
		/// </summary>
		public string MerchantId { set; get; }

		/// <summary>
		/// 商户名称
		/// </summary>
		public string MerchantName { set; get; }

		/// <summary>
		/// 手机号
		/// </summary>
		public string Mobile { set; get; }

		/// <summary>
		/// 区域ID
		/// </summary>
		public string AreaId { set; get; }

		/// <summary>
		/// 详细地址
		/// </summary>
		public string Address { set; get; }

		/// <summary>
		/// 有效标记
		/// </summary>
		public  int EnabledFlag { set; get; }

		/// <summary>
		/// 删除标记
		/// </summary>
		public  int DeleteFlag { set; get; }

		/// <summary>
		/// 备注
		/// </summary>
		public string Description { set; get; }

		/// <summary>
		/// 创建人ID
		/// </summary>
		public string CreateUserId { set; get; }

		/// <summary>
		/// 创建时间
		/// </summary>
		public DateTime? CreateDate { set; get; }

		/// <summary>
		/// 更新人ID
		/// </summary>
		public string ModifyUserId { set; get; }

		/// <summary>
		/// 更新时间
		/// </summary>
		public DateTime? ModifyDate { set; get; }
	}
}
