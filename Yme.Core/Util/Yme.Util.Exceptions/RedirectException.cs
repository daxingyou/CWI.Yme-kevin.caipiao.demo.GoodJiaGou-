using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Yme.Util.Exceptions
{
    /// <summary>
    /// 跳转异常
    /// </summary>
    public class RedirectException : Exception
    {
        /// <summary>
        /// 构告函数
        /// </summary>
        /// <param name="url">url</param>
        public RedirectException(string url)
        {
            this.Url = url;
        }

        public string Url { get; set; }
    }
}
